    // M√©todo p/ cadastrar um registro no banco de dados.
    private void cadastraRegistro() {
        // Antes de cadastrar, verifica se o usu√°rio est√° com algum registro selecionado
        if (jT1Nome.isEditable() && jT0Id.getText().equals("")) {
            // Antes de cadastrar, verifica se os campos foram preenchidos
            if (verificaDados()) {
                try {
                    Book l = new Book();

                    l.setNome(jT1Nome.getText());
                    l.setAuthor(jT2Author.getText());
                    l.setPublisher(jT3Publisher.getText());
                    l.setIsbn(jT4Isbn.getText());
                    l.setPreco(Double.valueOf(jT5Preco.getText()));

                    BdLivro d = new BdLivro();

                    d.adicionaLivro(l);

                    JOptionPane.showMessageDialog(rootPane, "Dados cadastrados com sucesso.");
                    limpaCampos();
                    desabilitaCampos();

                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(rootPane, "Erro ao cadastrar dados.");
                }
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Para cadastrar um novo registro.\nClique em 'Novo'.");
        }
    }
    
    // M√©todo p/ valida√ß√£o do formul√°rio
    private boolean verificaDados() {
        if ((!jT1Nome.getText().equals("")) && (!jT2Author.getText().equals("")) 
                && (!jT3Publisher.getText().equals("")) && (!jT4Isbn.getText().equals(""))
                && (!jT5Preco.getText().equals(""))) {
            return true;
        }
        JOptionPane.showMessageDialog(rootPane, "Dados imcompletos.");
        return false;
    }
    
